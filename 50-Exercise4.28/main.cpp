#include <iostream>

using std::cout;
using std::endl;

int main() {

    cout << "Size of a bool is: " << sizeof(bool) << " bytes" << endl;
    cout << "Size of a char is: " << sizeof(char) << " bytes" << endl;
    cout << "Size of a short int is: " << sizeof(short int) << " bytes" << endl;
    cout << "Size of an int is: " << sizeof(int) << " bytes" << endl;
    cout << "Size of a long int is: " << sizeof(long int) << " bytes" << endl;
    cout << "Size of a float is: " << sizeof(float) << " bytes" << endl;
    cout << "Size of a double is: " << sizeof(double) << " bytes" << endl;
    return 0;
}